public class Relational {
    public static void main(String[] args) {
        
        int x = 6;
        int y = 5;

        boolean result = x > y;
        // This will give whether the condition is true or false
        // here it contains all type of relational operators such as < , > , = , <= , >= , == and != .
        System.out.println(result);
    }
}
