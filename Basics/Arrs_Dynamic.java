package Basics;
public class Arrs_Dynamic {
    public static void main (String args[])
    {
        int nums[] = new int[4];

        nums[0] = 3;
        nums[1] = 1;
        nums[2] = 2;
        nums[3] = 8;

        
        for(int i = 0; i<4; i++)
        {
            System.out.println(nums[i]);

        }
    }
    
}
